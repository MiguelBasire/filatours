  function getMatchingStop(str) {
    var match = undefined;
    for (var s in _stops) {
      var se = _stops[s];
      if (str == se._name + ' (' + se._city + ')') {
        match = se;
        break;
      }
    }
    if (match == undefined) {
      throw new InvalidBusStopException; 
    }
    return match;
  }

